package entity;

import java.util.ArrayList;

public class Stage {
    private String name;
    private int time;
    private ArrayList<Action> actions;

    public Stage(String name, int time) {
        this.name = name;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
